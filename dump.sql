CREATE DATABASE JORNAL;

USE JORNAL;

CREATE TABLE USUARIO(
	USUARIO VARCHAR(20) PRIMARY KEY, 
	NOME VARCHAR(50),
	EMAIL VARCHAR(50),
	TELEFONE VARCHAR(20),
	PASSWORD VARCHAR(20)
);

CREATE TABLE NOTICIAS(
	TITULO VARCHAR(50) PRIMARY KEY,
	TIPO VARCHAR(20),
	JORNALISTA VARCHAR(20),
	DESCRICAO TEXT,
	DATA_CRIACAO DATE,
	APROVADOR_ID VARCHAR(20),
	APROVADOR_DATA DATE,
	FOREIGN KEY (APROVADOR_ID) REFERENCES USUARIO(USUARIO)
);